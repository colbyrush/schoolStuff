public class vector
{
    public double x,y,z,v;
    
    public vector(double x, double y, double z, double v)
    {
        this.x = x;
        this.y = y;
        this.z = z;
        this.v = v;
    }
    
    
    public void SetVector(double x, double y, double z, double v)
    {
        this.x = x;
        this.y = y;
        this.z = z;
        this.v = v;
    }
    
}